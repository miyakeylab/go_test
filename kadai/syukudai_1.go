func Syukudai_1() {
	fs := flag.NewFlagSet(os.Args[0], flag.ExitOnError)
	var (
		opt1 = fs.Bool("n", false, "改行フラグ")
		opt2 = fs.Bool("m", false, "メルカリフラグ")
	)

	arg1 := os.Args[1]
	fs.Parse(os.Args[2:])
	//fmt.Println("arg1:", arg1)
	//fmt.Println("opt1:", *opt1)
	//fmt.Println("opt2:", *opt2)
	//fmt.Println("args:", fs.Args())

	if arg1 == "mycat" {
		if *opt1 == true {
			var cnt int = 1
			for i := 0; i < fs.NArg(); i++ {

				sf, err := os.Open(fs.Arg(i))
				if err == nil {

					// 関数終了時に閉じる
					defer sf.Close()

					scanner := bufio.NewScanner(sf) // 1行ずつ読み込んで繰り返す
					for scanner.Scan() {
						//1行分を出力する
						fmt.Println(fmt.Sprintf("%d:%s",cnt,scanner.Text()))
						//fmt.Println(scanner.Text())
						cnt++
					}
				}
			}

		} else {

			for i := 0; i < fs.NArg(); i++ {

				sf, err := os.Open(fs.Arg(i))
				if err == nil {

					// 関数終了時に閉じる
					defer sf.Close()

					scanner := bufio.NewScanner(sf) // 1行ずつ読み込んで繰り返す
					for scanner.Scan() {
						//1行分を出力する
						fmt.Println(scanner.Text())
					}
				}
			}
		}

		if *opt2 == true {
			MerucariCreate()
		}
	}

}


